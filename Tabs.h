enum Type_t
{
    STRING,
    DOUBLE,
    BOOL
};

struct Value_t
{
    QString s;
    double d;
    bool b;

    Type_t type;
};

static Value_t* value(QString s)
{
    Value_t * v = new Value_t;

    v->s = s;
    v->type = Type_t::STRING;

    return v;
}
static Value_t* value(double d)
{
    Value_t * v = new Value_t;

    v->d = d;
    v->type = Type_t::DOUBLE;

    return v;
}
static Value_t* value(bool b)
{
    Value_t * v = new Value_t;

    v->b = b;
    v->type = Type_t::BOOL;

    return v;
}

